/**
 * identifies config DB (parts) that are read only
 */
@Qualifier
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD, ElementType.FIELD, ElementType.PARAMETER, ElementType.TYPE})
public @interface JSONReadonlyDatabase {
}
